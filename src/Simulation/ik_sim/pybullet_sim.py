def set_joint_angles(arm, angles_deg):
    """
    set the joint angles of the simulated robot and return the resulting end-effector pose
    angles_deg: list of 6 joint angles in degrees
    returns: (error_code, [x_mm, y_mm, z_mm, rx_deg, ry_deg, rz_deg])
    """
    client, robot_id = arm['client'], arm['robot_id']
    for joint_idx, angle_rad in zip(arm['joint_indices'], np.deg2rad(angles_deg)):
        p.resetJointState(robot_id, joint_idx, float(angle_rad), physicsClientId=client)

    state = p.getLinkState(robot_id, arm['eef_link'],
                           computeForwardKinematics=True, physicsClientId=client)
    pos_mm = np.array(state[4]) * 1000.0          # meters → mm
    orn_deg = _quat_to_rotvec_deg(state[5])        # quaternion → rotvec deg
    return 0, list(pos_mm) + list(orn_deg)

def get_joint_angles(arm, is_radian=False):
    angles = np.array([
        p.getJointState(arm['robot_id'], j, physicsClientId=arm['client'])[0]
        for j in arm['joint_indices']
    ])
    return angles if is_radian else np.rad2deg(angles)

def _quat_to_rotvec_deg(q_xyzw):
    rotvec_rad = Rotation.from_quat(q_xyzw).as_rotvec()
    return np.rad2deg(rotvec_rad)
