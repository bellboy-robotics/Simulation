import numpy as np
import pybullet as p
from scipy.spatial.transform import Rotation

TCP_OFFSET = [0, 0, 200, 0, 0, 0]  # mm/deg — must match ufactory_sim

_URDF_PATH = '/tmp/urdf/XI130506F56A19-with-tcp.urdf'  # generated by pyroki on startup

def set_pybullet(urdf_path=None):
    import pybullet as p
    if urdf_path is None:
        urdf_path = _URDF_PATH

    client = p.connect(p.DIRECT)
    robot_id = p.loadURDF(urdf_path, useFixedBase=True, physicsClientId=client)

    joint_indices = []
    eef_link_index = None
    for i in range(p.getNumJoints(robot_id, physicsClientId=client)):
        info = p.getJointInfo(robot_id, i, physicsClientId=client)
        link_name = info[12].decode('utf-8').lower()
        if info[2] != p.JOINT_FIXED:
            joint_indices.append(i)
        if any(k in link_name for k in ('tcp', 'eef', 'tool')):
            eef_link_index = i

    if eef_link_index is None:
        eef_link_index = p.getNumJoints(robot_id, physicsClientId=client) - 1

    return {
        'client': client,
        'robot_id': robot_id,
        'joint_indices': joint_indices[:6],
        'eef_link': eef_link_index,
    }

